function playGame(){
	let cells = document.querySelectorAll('.cell'),
	player = document.querySelector('.player'),
	winnerText = document.querySelector('.winner'),
	playerTurn = "X",
	count = 0;
	
	cells.forEach(cell => {
		if(gridSize.value == "3x3") cell.style.fontSize = "50px";
		else if(gridSize.value == "4x4") cell.style.fontSize = "60px";
		else if(gridSize.value == "5x5") cell.style.fontSize = "40px";
		else if(gridSize.value == "6x6") cell.style.fontSize = "30px";
		else if(gridSize.value == "7x7") cell.style.fontSize = "22px";
		else if(gridSize.value == "8x8") cell.style.fontSize = "20px";
		cell.addEventListener('click', () => {
			count++;
			if (cell.innerHTML !== "") return;
			cell.innerHTML = playerTurn;
			if (count == cellNum) {
				winnerText.innerHTML = "Match tied";
				inactiveCells();
			}
			checkForWin();
			playerTurn = playerTurn == "X" ? "O" : "X";
			player.innerHTML = `${playerTurn}'s turn`;
		});
	});

	function checkForWin(){
		winningCombinations.forEach(combination => {
			let check = combination.every(idx => cells[idx].innerHTML == playerTurn);
			if(check){
				highlightCells(combination);
				inactiveCells();
				winnerText.innerHTML = `${playerTurn} wins`;
				if(playerTurn == "X"){
					xScore++;
					xScoreDisplay.innerHTML = xScore;
				}
				else{
					oScore++;
					oScoreDisplay.innerHTML = oScore;
				}
			}
		});
	}

	function highlightCells(combination){
		combination.forEach(idx => {
			cells[idx].classList.add('highlight');
		});
	}

	function inactiveCells(){
		cells.forEach(cell => {
			cell.classList.add('inactive');
		});
	}

	function newGame(){
		gameLevel.classList.remove('hide');
		game.classList.add('hide');
		board.innerHTML = "";
		gridSize.value = "";
		matchOptions.innerHTML = '<option value="">Select number of matches</option>';
		xScore = 0; oScore = 0;
		xScoreDisplay.innerHTML = xScore;
		oScoreDisplay.innerHTML =	oScore;
		winnerText.innerHTML = "";
	}

	function restartGame(){
		count = 0;
		cells.forEach(cell => {
			cell.classList.remove('inactive');
			cell.classList.remove('highlight');
			cell.innerHTML = "";
		});
		winnerText.innerHTML = "";
	}

	newGameBtn.addEventListener('click', newGame);
	restartBtn.addEventListener('click', restartGame);
}